									<p>Jeruk siam pontianak merupakan salah satu jenis jeruk siam yang berasal dari Kalimantan Barat, tepatnya di daerah Pontianak. Seperti yang kita tahu, jenis jeruk siam sendiri ada banyak dan berasal dari daerah yang berbeda – beda. Sebagai contoh meskipun sama – sama jeruk siam, namun jeruk siam dari daerah A dengan daerah B tentunya memiliki keunggulan dan ciri khas masing – masing.
                                        <span>Hal tersebut juga berlaku bagi jeruk siam pontianak. Salah satu keunggulan dari jeruk siam ini terletak pada citarasa buahnya yang sangat manis, dipadukan dengan aromanya yang harum serta kandungan airnya yang melimpah sehingga akan terasa sangat menyegarkan. Ketika dikunyah, Anda akan dimanjakan dengan bulir – bulir jeruk yang bertekstur renyah dan lembut dilidah.</span>
                                        <span>Selain lezat, jeruk siam pontianak juga dikenal memiliki tingkat produktivitas buah yang cukup tinggi. Pada panen pertama atau saat masih belajar berbuah, satu pohonnya bisa menghasilkan sekitar 5 kilogram buah jeruk. Sedangkan jika sudah dewasa, satu pohonnya mampu memproduksi hingga 20 kilogram buah jeruk. Hebat bukan?</span>
                                        <span>Secara morfologi, tanaman jeruk siam pontianak termasuk kedalam jenis tanaman berkayu yang tumbuh tegak, batangnya cukup keras, berbentuk bulat, memiliki percabangan yang banyak dan tajuk yang sangat rimbun. Di habitat aslinya, tanaman ini dapat tumbuh hingga mencapai 8 meter. Namun jika ditanam dipemukiman, biasanya menjadi tidak terlalu tinggi.</span>
                                        <span>Daunnya berwarna hijau, berjenis menyirip, berbentuk oval memanjang, ujungnya meruncing, dan sedikit bergerigi pada bagian tepinya. Bunganya berukuran kecil, berwarna putih bersih, dan biasanya muncul pada ketiak daun. Bunganya ini sangat disukai oleh serangga seperti lebah dan kupu – kupu karena banyak mengandung nektar.</span>
                                        <span>Sedangkan untuk buahnya, buah jeruk siam pontianak berbentuk bulat, beratnya sekitar 120 - 250 gram, dan sedikit berlekuk pada bagian atas dan pangkal buah. Kulitnya berwarna kuning kehijauan, cukup tipis, permukaannya halus berpori. Daging buahnya tebal, lembut, berserat halus, berwarna kuning dan terdapat biji – biji kecil di dalamnya.</span>
                                        <span>Jeruk siam pontianak dapat tumbuh subur pada daerah dataran rendah maupun daerah dataran tinggi mencapai 700 mdpl dengan suhu udara berkisar antara 25 - 35°C. Jeruk ini sangat menyukai media tanam berupa tanah humus yang gembur dan subur. Selain itu, pastikan lahan penanaman memiliki sistem drainase yang baik karena jeruk ini tidak menyukai air yang menggenang.</span>
                                        <span>Meskipun demikian, namun tanaman jeruk siam pontianak tetap membutuhkan air untuk pertumbuhannya. Maka dari itu, lakukan penyiraman setiap dua kali sehari (pagi dan sore) pada musim kemarau. Sedangkan pada musim hujan, intensitas penyiraman sebaiknya disesuaikan dengan kondisi cuaca dan kondisi media tanam.</span>
                                        <span>Nah yang tidak boleh ketinggalan dalam budidaya jeruk siam pontianak adalah pemupukan. Untuk pemupukan dapat dilakukan setiap sebulan sekali menggunakan pupuk NPK atau pupuk kandang. Jika dirawat dengan baik, bibit jeruk siam pontianak hasil cangkok/okulasi biasanya akan mulai berbuah dalam waktu 1 – 2 tahun sejak penanaman.</span>
                                        <span>Meskipun ukurannya tidak terlalu besar, namun di dalam buah jeruk siam pontianak ternyata tersimpan berbagai macam manfaat untuk kesehatan. Selain itu, di dalam buahnya juga kaya akan kandungan gizi seperti  vitamin C, vitamin B6, magnesium, serat. Manfaat jeruk siam pontianak untuk kesehatan diantaranya :<br>
                                            1. Mencegah radikal bebas. <br>
                                            2. Meningkatkan energi. <br>
                                            3. Menjaga tekanan darah. <br>
                                            4. Meningkatkan sistem imun. <br>
                                            5. Mengurangi risiko kanker. <br>
                                            6. Mencegah sembelit. <br>
                                            7. Menjaga kadar kolesterol. <br>
                                            8. Meningkatkan kesehatan kulit. <br>
                                            9. Mencegah sariawan. <br>
                                            10. Baik untuk kesehatan mata. <br>
                                        </span>
