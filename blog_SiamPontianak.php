									<p>Pontianak siam orange is one type of siam orange originating from West Kalimantan, to be precise in the area of ​​Pontianak. As we know, there are many types of Siamese oranges and come from different regions. For example, even though they are both Siamese oranges, the Siamese oranges from region A to region B certainly have their respective advantages and characteristics.
                                        <span>This also applies to Pontianak siam oranges. One of the advantages of this Siamese orange lies in the very sweet taste of the fruit, combined with its fragrant aroma and abundant water content, so it will feel very refreshing. When chewed, you will be spoiled with orange pulp which is crunchy and soft on your tongue.</span>
                                        <span>Besides being delicious, Pontianak siam oranges are also known to have a high level of fruit productivity. At the first harvest or while still learning to bear fruit, one tree can produce about 5 kilograms of oranges. Meanwhile, when it is grown, one tree can produce up to 20 kilograms of citrus fruit. Isn't that great?</span>
                                        <span>Morphologically, the Pontianak siam citrus plant is a type of woody plant that grows upright, has a fairly hard stem, is round in shape, has many branches and a very lush canopy. In their natural habitat, this plant can grow up to 8 meters. However, if it is planted in a settlement, it will usually not be too high.</span>
                                        <span>The leaves are green, pinnate type, elongated oval, tapered edges, and slightly serrated at the edges. The flowers are small, clean white, and usually appear on the axillary leaves. These flowers are very popular with insects such as bees and butterflies because they contain a lot of nectar.</span>
                                        <span>As for the fruit, the Pontianak siam oranges are round in shape, weigh about 120 - 250 grams, and are slightly grooved at the top and base of the fruit. The skin is greenish yellow, quite thin, the surface is smooth, porous. The pulp is thick, soft, fibrous, yellow and there are small seeds in it.</span>
                                        <span>Pontianak siam oranges can thrive in lowland and highland areas reaching 700 meters above sea level with air temperatures ranging from 25 - 35 ° C. This orange really likes planting media in the form of loose and fertile humus soil. Also, make sure the planting area has a good drainage system as these oranges do not like stagnant water.</span>
                                        <span>Even so, the Pontianak siam citrus plant still needs water for its growth. Therefore, do watering twice a day (morning and evening) during the dry season. Meanwhile, during the rainy season, the intensity of watering should be adjusted to weather conditions and growing media conditions.</span>
                                        <span>So what should not be left behind in Pontianak siam citrus cultivation is fertilization. Fertilization can be done once a month using NPK or manure. If cared for properly, the grafted / grafted siam pontianak seeds will usually begin to bear fruit within 1-2 years of planting.</span>
